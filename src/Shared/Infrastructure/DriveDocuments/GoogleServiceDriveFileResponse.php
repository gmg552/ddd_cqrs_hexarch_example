<?php

namespace Qalis\Shared\Infrastructure\DriveDocuments;

final class GoogleServiceDriveFileResponse {

    private string $id;
    private string $name;

    public function __construct(string $id, string $name)
    {
        $this->id = $id;
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function id(): string
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function name(): string
    {
        return $this->name;
    }

}
